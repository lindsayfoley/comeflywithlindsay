					<p>Unfortunately for us, we arrived during off season which meant most things were closed.</p>
					<figure>
						<img src="images/whistler/lost-lake-whistler-behind-trees.jpg" alt="The Lost Lake behind the trees in Whistler, Canada">
						<figcaption>The Lost Lake behind the trees in Whistler</figcaption>	
					</figure>	
					
					<p>We stayed in Whistler for 3 nights in October 2017 and I would say this is the perfect amount of time. When I return, I'd like to do the activities that were closed, try out the gondola.</p>			
					<hr>
					
					<h2>My Top Tips</h2>
					<ul>
						<li>Check the schedule - you want to make sure it's not between seasons</li>
						<li>Rent a bike - there's loads of places to do this and plenty of safe cycle lanes</li>
						<li>Book restaurants - it gets very busy in this little town and make bookings</li>
