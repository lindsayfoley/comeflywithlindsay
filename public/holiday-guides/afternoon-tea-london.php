					<h3>Traditional Afternoon Tea At The Ritz - Mayfair</h3>
					<p>It was a joy to dine in such an elegant setting with striking floral arrangements and opulent chandeliers which  perfectly complimented the Ritz' Renaissance decor. The sandwiches here were delicious and moreish, we easily made our way through 2 plates of these! Both scones and cakes were light and very tasty, and we were offered a second helping of these dishes. The service was great, a bit fastidious at times and not as relaxed as Fortnum &amp; Mason, but overall it was a lovely, memorable dining experience.</p>
					<p>Afternoon tea at <a target="_blank" href="https://www.theritzlondon.com/dine-with-us/afternoon-tea/">The Ritz</a> is priced at &pound;54 per person and is ideal for celebrating special occasions or purchased for a loved one as a gift.</p>
