					<h1>Afternoon Tea in London, England</h1>
					<p>I've been fortunate to experience traditional afternoon tea in some of London's most special venues. With so many options available today, I've put together this little guide to showcase my top picks, listed in order of my preference to provide you with a helping hand!</p>
					<hr>
					
					<h3>Traditional Afternoon Tea At Fortnum &amp; Mason - Mayfair</h3>
					<p>Afternoon tea takes place in the Diamond Jubilee Tea Salon, located on the top floor of this famous department store. The tea salon is just lovely and is the perfect setting to enjoy a luxurious tea, featuring exquisite food and outstanding 5-star service.</p>
					<p>We were given a great selection of teas and the usual finger sandwiches which I really enjoyed but the sweet treats are really the showstoppers here. The scones were delicious and served with lemon curd, clotted cream and strawberry jam, and the top tier of the cake stand featured rich, flawless looking pastries. The tea salon has a calm atmosphere, with live music being played on the grand piano - you can easily forget you're dining in a busy department store in the heart of London! At the end of our tea, our waiter very kindly packed up our lemon curd, jam, leftover pastries and offered us slices of cakes that we were too full to try so we could enjoy these at home!</p>
					<p>The <a target="_blank" href="https://www.fortnumandmason.com/restaurants/afternoon-tea">Fortnum &amp; Mason</a> tea is &pound;44 per person and is more laid back than tea at The Ritz or The Dorchester, especially as you don't have to be suited and booted here! The service is top notch and the setting is beautiful, making it the perfect choice for all occasions.</p>
