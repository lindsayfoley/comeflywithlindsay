					<h3>Tale As Old As Time Tea, Town House At The Kensington - Kensington</h3>
					<p>After months of trying, I finally managed to get a booking at this extremely popular tea! Being themed around my favourite childhood Disney film, I had high hopes for the menu and was interested in seeing their execution. Upon arrival my first thoughts were that the Town House Hotel is beautiful, with tasteful decor and a cosy tea room featuring some familiar faces; Lumi√®re and Cogsworth.</p>
					<p>The afternoon tea includes a first course of savoury foods - I won't ruin it and go into too much detail but it's a nice way to kick things off. The presentation of the table and food is brilliant, a lot of effort and planning has gone into the menu and it pays off. You do get very full eating a savoury course before the sandwiches and cakes, but it's very good value. The service was spot on and the waiter took time to explain each food item to us in detail. Surprisingly, there were no scones (whatttt?) which is really strange for an afternoon tea! Overall, this whimsical tea is a must for Beauty and The Beast fans and features some extra surprises which I still won't reveal.</p>
					<p>The Tale As Old As Time Tea at <a target="_blank" href="http://townhousekensington.com/tale-as-old-as-time/">Town House at The Kensington</a> is &pound;38.50 per person and is ideal for Disney enthusiasts of all ages.</p>
