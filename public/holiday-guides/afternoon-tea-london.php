					<h3>Traditional Afternoon Tea At The Ritz - Mayfair</h3>
					<p>The Ritz oozes elegance; when you first enter the hotel and walk into The Palm Court you can't help but take note of the striking floral arrangements and opulent chandeliers which perfectly compliment this hotels Renaissance decor.<p>
						<p>You get the usual line up of little sandwiches which were delicious and moreish, we easily made our way through two plates of these. Both scones and cakes not only looked great, they were light and tasty and we were fortunate to be offered a second helping of everything on the cake stand. The service was great, a bit fussy at times but overall it was a lovely, memorable dining experience!</p>
					<p>Afternoon tea at <a target="_blank" href="https://www.theritzlondon.com/dine-with-us/afternoon-tea/">The Ritz</a> is priced at &pound;54 per person and is an ideal venue for celebrating special occasions. If you choose to dine here, you'll have to adhere to a dress code, even for afternoon tea - so no jeans allowed and men must wear a jacket and a tie.</p>
