					<h1>Afternoon Tea in London, England</h1>
					<p>I've been fortunate to experience traditional afternoon tea in some of London's most exquisite venues. With so many options available, I've put together this little guide to showcase my top picks, listed in order of my preference to provide you with a helping hand!</p>
					<hr>
					
					<h3>Traditional Afternoon Tea At Fortnum &amp; Mason - Mayfair</h3>
					<p>The Diamond Jubilee Tea Salon is just lovely and is the perfect setting to enjoy a luxurious afternoon tea, featuring exquisite food and outstanding 5-star service. We were given a great selection of teas and sandwiches which I enjoyed, but the sweet treats are really the showstoppers here. The scones were delicious and served with lemon curd, clotted cream and strawberry jam and the top tier of the cake stand featured rich, flawless looking pastries. The tea salon has a tranquil atmosphere, with live music being played on the grand piano - you can easily forget you're dining in a busy department store in the heart of London! At the end of our tea, our waiter very kindly packed up our lemon curd, jam, leftover pastries and offered us slices of cakes that we were too full try, so we could enjoy these at home.</p>
					<p>The <a target="_blank" href="https://www.fortnumandmason.com/restaurants/afternoon-tea">Fortnum &amp; Mason</a> tea is &pound;44 per person and is more laid back than tea at The Ritz/Dorchester, making it perfect for all occasions.</p>
