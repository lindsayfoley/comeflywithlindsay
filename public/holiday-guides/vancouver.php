					<h1>Downtown Vancouver, Canada</h1>
					<p>I can see why Downtown Vancouver is such a popular destination, it's a city that has it all; mountains, ocean, forests, parks and beaches. The skyscraper buildings and business districts are reminiscent of New York but on a much smaller scale and nowhere near as busy. There's a lot to see and do in Van and if you're lucky you may even see something being filmed!</p>
					<figure>
						<img src="/images/vancouver/downtown-vancouver-at-night.jpg" alt="Robson Street in Downtown Vancouver at night, Canada">
						<figcaption>Robson Street in Downtown Vancouver at night</figcaption>
					</figure>
					
					<p>We had a nice, comfortable journey travelling from Whistler Village to Downtown Vancouver on the <a target="_blank" href="http://www.pacificcoach.com/">Pacific Coach</a>. We stayed at a 4-star hotel on Robson Street which location wise was perfect, however the hotel standards were pretty shocking so I won't be mentioning the name of it! You could stay in any of the districts in Downtown Vancouver and manage to walk to most places, so there really are no bad decisions you can make based on location. The Theatre district and historic districts of Gastown and Yaletown were my favourite spots.</p>
					<figure>
						<img src="/images/vancouver/steam-clock-gastown-vancouver.jpg" alt="The steam clock in Gastown, Downtown Vancouver, Canada">
						<figcaption>The steam clock in Gastown, Downtown Vancouver</figcaption>
					</figure>	

					
					<p>Coal Harbour and Stanley Park are exceptional, when the sun is out I recommend renting a bike for a few hours and cycling around these areas. We rented bikes from <a target="_blank" href="http://www.spokesbicyclerentals.com/">Spokes</a>, located near Stanley Park and cycled leisurely around Stanley Park in about an hour. We stopped frequently to take pictures of the scenery and occasionally I fell off my bike when other cyclists yelled 'coming through' and overtook me!</p>

					<figure>
						<img src="/images/vancouver/cycling-around-stanley-park-in-vancouver.jpg" alt="Cycling around Stanley Park in Vancouver, Canada">
						<figcaption>Cycling around Stanley Park in Vancouver</figcaption>
					</figure>				
					
					<p>There are so many areas all in walking distance in Downtown Vancouver and it's really not far to walk from one end of the city to the other end. One evening we walked from Gastown to Sunset Beach which took about 50 minutes going at a relaxed pace.</p>
					<figure>
						<img src="/images/vancouver/sunset-beach-vancouver-canada.jpg" alt="Sunset Beach at dusk in Downtown Vancouver, Canada">
						<figcaption>Sunset Beach at dusk in Downtown Vancouver</figcaption>	
					</figure>						
					
					<p>If you're looking for an indoor activity which isn't too expensive, I highly recommend a visit to <a target="_blank" href="http://www.vanaqua.org/">Vancouver Aquarium</a> which is located near Stanley Park. It's not a huge aquarium but it's certainly a good way to kill a few hours, especially when there is torrential rainfall outside! My personal highlights were seeing the otters, penguins, sea lions and listening to the informative 	talks about sharks.</p>
					<figure>
						<img src="/images/vancouver/penguins-at-vancouver-aquarium-canada.jpg" alt="Visiting the penguins at Vancouver Aquarium, Canada">
						<figcaption>Visiting the penguins at Vancouver Aquarium</figcaption>
					</figure>		
					<p>We stayed in Vancouver for 4 nights and I could have easily stayed another 2. It does rain a lot which may hinder your plans for outdoor activities. On my return, I'll visit <a target="_blank" href="https://www.capbridge.com/">Capilano Suspension Bridge</a>, Granville Island, VanDusen Botanical Garden and Queen Elizabeth Park.</p>					
					<hr>
					
					<h2>My Top Tips</h2>
					<ul>
						<li>Pick the right hotel - I got this so very wrong, so please make sure you choose one rated highly which has consistent reviews</li>
						<li>It's raining, it's pouring - I wasn't quite ready for the amount of rain in Vancouver! Bring a brolly, waterproof shoes and a raincoat</li>
						<li>These boots are made for walking - there are loads of lovely little districts to be explored, venture out to all/most of them</li>
					</ul>
