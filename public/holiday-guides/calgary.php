					<h1>Downtown Calgary, Canada</h1>
					<p>There's not a whole lot to do in Downtown Calgary, it seems to be more of a town that people pass through on their way to visit <a href="holiday-guides/banff">Banff</a> to see the Canadian Rocky mountains. I've put together a concise guide on what to do if you're there for a day or two.</p>
					
					<p>We stayed at the <a target="_blank" href="http://www.fairmont.com/palliser-calgary/">Fairmont Palliser Hotel</a> as the room price was incredibly reasonable for such a luxury hotel chain! The location is ideal, as it's in the heart of the city and just a few minutes walk from the Calgary Tower which you can probably see from your hotel room window. This hotel is grand, boasting a lavish foyer with elegant chandeliers, striking marble pillars and rich gold accents.</p>
					
					<figure>
						<img src="/images/calgary/fairmont-palliser-lobby-downtown-calgary.jpg" alt="The lobby at the Fairmont Palliser in Downtown Calgary">
						<figcaption>The lobby at the Fairmont Palliser in Downtown Calgary</figcaption>
					</figure>
	
					<p>Our room was generous in size, especially for a standard, and to my delight it was recently refurbished. It was pretty quiet so we slept fairly well, apart from hearing the odd few trains passing through. We ordered room service a few times when suffering from jet-lag and I have to say the the food was consistently outstanding, it was pricey but well worth it! The facilities include an indoor pool, spa and a Starbucks located on the lower ground floor which was useful when we needed water, snacks and of course hot drinks to warm us up.</p>
					<figure>
						<img src="/images/calgary/fairmont-queen-room-fairmont-palliser.jpg" alt="The Fairmont Queen room at the Fairmont Palliser">
						<figcaption>The Fairmont Queen room at the Fairmont Palliser</figcaption>
					</figure>					
					
					<p>We walked down to the Olympic Plaza on Stephen Avenue, where medals were presented in the 1988 Winter Olympics. There were no events on when we visited, but it was still a nice place for a stroll on a sunny day. The plaza is well maintained and has a water feature in the center with the Canadian Maple leaf, surrounded by small fountains. Definitely worth a pit-stop, but don't go out of your way to visit.</p>
					<figure>
						<img src="/images/calgary/olympic-plaza-calgary.jpg" alt="Olympic Plaza in Downtown Calgary">
						<figcaption>Olympic Plaza in Downtown Calgary</figcaption>
					</figure>
										
					<p>We went to <a target="_blank" href="http://www.calgarytower.com/">Calgary Tower</a> one evening for dinner and to checkout the 360-degree views of the city from the observation deck. We chose to eat at the revolving restaurant, <a target="_blank" href="http://www.sky360.ca/">Sky 360</a> which was definitely a unique dining experience with pleasant food and excellent service. Top tip... don't leave your handbag on the window ledge, I learned the hard way that this doesn't rotate with you!</p>
					<figure>
						<img src="/images/calgary/sky-360-restaurant-calgary-tower.jpg" alt="The observation deck in Calgary Tower">
						<figcaption>Don't look down... the observation deck in Calgary Tower</figcaption>
					</figure>	
					
					<p>If you fancy going shopping, catch an Uber for &dollar;15 to the <a href=""https://www.cfshops.com/chinook-centre.html>Chinook Centre</a> which is a 15 minute drive south. It's a large shopping centre with a vast selection of fashion, beauty and homeware stores including a Nordstrom, a food court, cinema and bowling alley.</p>				
					
					<p>We stayed in Calgary for 2 nights in October 2017, over the Canadian Thanksgiving period (second Monday in October) which may be the reason why it was tremendously quiet. That said, I wouldn't plan to stay here for an extended period of time, a weekend would do fine!</p>			
					<hr>
					
					<h2>My Top Tips</h2>
					<ul>
						<li>Dinner with a difference - if you're thinking of booking <a target="_blank" href="http://www.sky360.ca/">Sky 360</a> for dinner, time your reservation at sunset for a spectacular view</li>
						<li>Wrap up warm - it was snowing in early October and was bitterly cold! Coats, scarves, gloves, hats are a must</li>
						<li>Time well spent - if you're heading to Banff, consider extending your stay to spend more time there and less time in Calgary</li>
